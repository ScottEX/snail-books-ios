let curLang = (typeof localStorage !== 'undefined' && localStorage.getItem('lang')) || 'zh-CN';
if (typeof navigator !== 'undefined') {
  const bl = navigator.language || '';
  if (!localStorage.getItem('lang')) {
    if (bl.startsWith('zh-TW') || bl.startsWith('zh-HK')) curLang = 'zh-TW';
    else if (bl.startsWith('zh')) curLang = 'zh-CN';
    else if (bl.startsWith('en')) curLang = 'en';
  }
}

export function t(key: string): string {
  return I18N[curLang]?.[key] || I18N['zh-CN']?.[key] || key;
}

export function getLang(): string {
  return curLang;
}

export function setLang(lang: string, callback?: () => void) {
  if (!I18N[lang]) return;
  curLang = lang;
  if (typeof localStorage !== 'undefined') localStorage.setItem('lang', lang);
  // Save to backend so language follows the user across devices
  try {
    fetch('/api/settings/lang', {
      method: 'PUT',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ lang }),
    }).catch(() => {});
  } catch {}
  callback?.();
}

